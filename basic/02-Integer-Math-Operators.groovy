class IntegersMathOperators {
    static main(args) {
        // The basic integer math operators
        println("7 + 4 = " + (7 + 4));
        println("7 - 4 = " + (7 - 4));
        println("7 * 4 = " + (7 * 4));
        println("7 / 4 = " + (7.intdiv(4)));
        println("7 % 4 = " + (7 % 4))
    }
}