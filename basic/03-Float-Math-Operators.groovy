class FloatMathOperators {
    static main(args) {
        // Floating point math operators
        println("7.2 + 4.4 = " + (7.2.plus(4.4)));
        println("7.2 - 4.4 = " + (7.2.minus(4.4)));
        println("7.2 * 4.4 = " + (7.2.multiply(4.4)));
        println("7.2 / 4.4 = " + (7.2 / 4.4));
    }
}